        <div class="modal-body modal-body-free-text" data-language='modal__step_5_explanation' data-language-original="">
         	<p>
         		<b>Total area of suite perimeter walls</b>: This is the sum of the areas of interior-facing perimeter walls.
         	</p>
         	<p>
         		<b>Total area of suite ceiling area</b>: This is the area of the ceiling.
         	</p>
         	<p>
         		<b>Exposed beams and columns</b>: This is the total area of any beams and columns that are exposed to fire, which are all faces that are not protected by gypsum-board encapsulation and are not embedded onto the ceiling or any walls.
         	</p>
         	<p>
         		<b>Exposed walls</b>: This is the total area of any mass timber walls (perimeter walls and interior walls) that are exposed to fire, which are all faces that are not protected by gypsum-board encapsulation and no objects such as beams, columns, mass timber walls and lightframe walls are embedded on them.
         	</p>
         	<p>
         		<b>Exposed ceiling</b>: This is the area of the ceiling that is not protected by gypsum-board encapsulation and is not embedded by an object such as beams, columns, and internal walls (mass timber walls and lightframe walls).
         	</p>
         	<p>
         		<b>Maximum FSR of exposed beams and columns</b>: This is the largest FSR (Flame Spread Rate) of any beams and columns that have at least one face that is exposed to fire (i.e. not encapsulated by a gypsum board and not embedded into a wall or ceiling).
         	</p>
         	<p>
         		<b>Maximum FSR of exposed walls</b>: This is the largest FSR (Flame Spread Rate) of any mass timber walls (perimeter walls and interior walls) that are exposed to fire, which are all faces that are not protected by gypsum-board encapsulation and no objects such as beams, columns, mass timber walls and lightframe walls are embedded on them.
         	</p>
         	<p>
         		<b>FSR of ceiling, if it is exposed</b>: This is the FSR (Flame Spread Rate) of the ceiling, if it's not fully encapsulated by a gypsum board. If it is, this will show up as Less than or equal to 75.
         	</p>
         	<p>
         		<b>Are there any exposed walls?</b>: If there is any perimeter walls and internal mass timber walls that are not fully encapsulated by a gypsum board.
         	</p>
         	<p>
         		<b>Are there any exposed walls that are less than 4.5 metres apart?</b>: If there are any perimeter walls and internal mass timber walls that are not fully encapsulated by a gypsum board that are less than 4.5 m apart.
         	</p>
